export function translateText(text: string, targetLanguage: string): string {
  if (targetLanguage === 'en') {
    return text;
  }
  
  // First try exact match
  if (translations[text] && translations[text][targetLanguage]) {
    return translations[text][targetLanguage];
  }
  
  let translatedText = text;
  
  // Only match full strings (trimmed/case-insensitive) to avoid mixed-language fragments
  const keys = Object.keys(translations);
  for (const key of keys) {
    const target = translations[key][targetLanguage];
    if (!target) continue;
    const a = translatedText.trim();
    const b = key.trim();
    if (a === b || a.toLowerCase() === b.toLowerCase()) {
      return target;
    }
  }
  
  return translatedText;
}

// Store original text content for restoration
const originalTexts = new Map<Element, string>();

export function translatePage(targetLanguage: string) {
  // Set document language and direction
  const rtlLangs = ['ar', 'he', 'fa'];
  document.documentElement.setAttribute('lang', targetLanguage);
  document.documentElement.setAttribute('dir', rtlLangs.includes(targetLanguage) ? 'rtl' : 'ltr');

  // First, restore all original texts if switching languages
  originalTexts.forEach((originalText, element) => {
    if (element.textContent && element.textContent !== originalText) {
      element.textContent = originalText;
    }
  });
  
  if (targetLanguage === 'en') {
    // Just return after restoring original texts
    return;
  }
  
  // Clear existing stored texts and remove translated markers
  document.querySelectorAll('.translated').forEach(el => {
    el.classList.remove('translated');
  });
  
  // Function to translate a single element
  const translateElement = (element: Element) => {
    if (element.textContent && 
        element.children.length === 0 && // Only translate leaf nodes
        element.textContent.trim().length > 0 &&
        !element.closest('script') && // Skip script tags
        !element.closest('style') && // Skip style tags
        !element.closest('noscript')) { // Skip noscript tags
      
      // Store original text if not already stored
      if (!originalTexts.has(element)) {
        originalTexts.set(element, element.textContent.trim());
      }
      
      const originalText = element.textContent.trim();
      const translated = translateText(originalText, targetLanguage);
      if (translated !== originalText) {
        element.textContent = translated;
        element.classList.add('translated');
      }
    }
  };
  
  // Comprehensive element selection - target everything with text
  const allTextElements = document.querySelectorAll('*');
  allTextElements.forEach(translateElement);
  
  // Use MutationObserver to catch dynamically loaded content
  const observer = new MutationObserver((mutations) => {
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        if (node.nodeType === Node.ELEMENT_NODE) {
          const element = node as Element;
          
          // Translate the new element if it has text content
          if (element.textContent) {
            // Store original text if not already stored
            if (!originalTexts.has(element)) {
              originalTexts.set(element, element.textContent.trim());
            }
            
            const originalText = element.textContent.trim();
            const translated = translateText(originalText, targetLanguage);
            if (translated !== originalText) {
              element.textContent = translated;
              element.classList.add('translated');
            }
          }
          
          // Also check child elements
          const childElements = element.querySelectorAll('*');
          childElements.forEach((child) => {
            if (child.textContent && child.children.length === 0) {
              // Store original text if not already stored
              if (!originalTexts.has(child)) {
                originalTexts.set(child, child.textContent.trim());
              }
              
              const originalText = child.textContent.trim();
              const translated = translateText(originalText, targetLanguage);
              if (translated !== originalText) {
                child.textContent = translated;
                child.classList.add('translated');
              }
            }
          });
        }
      });
    });
  });
  
  // Start observing
  observer.observe(document.body, {
    childList: true,
    subtree: true
  });
  
  // Stop observing after 5 seconds
  setTimeout(() => {
    observer.disconnect();
  }, 5000);
  
  console.log(`Page translated to ${targetLanguage}`);
}
export function translateText(text: string, targetLanguage: string): string {
  if (targetLanguage === 'en') {
    return text;
  }
  
  // First try exact match
  if (translations[text] && translations[text][targetLanguage]) {
    return translations[text][targetLanguage];
  }
  
  // Try to find partial matches for longer texts
  for (const key in translations) {
    if (text.includes(key) && translations[key][targetLanguage]) {
      return text.replace(key, translations[key][targetLanguage]);
    }
  }
  
  return text; // Return original if no translation found
}

export function translatePage(targetLanguage: string) {
  if (targetLanguage === 'en') {
    // Reload page to restore original text
    window.location.reload();
    return;
  }
  
  // Translate navigation elements
  const navElements = document.querySelectorAll('nav a, nav button');
  navElements.forEach((element) => {
    if (element.textContent) {
      const translated = translateText(element.textContent.trim(), targetLanguage);
      if (translated !== element.textContent.trim()) {
        element.textContent = translated;
      }
    }
  });
  
  // Translate main content elements
  const contentSelectors = [
    'h1', 'h2', 'h3', 'h4', 'h5', 'h6',
    'p:not(.translated)',
    'button:not(.translated)',
    '.text-4xl', '.text-3xl', '.text-2xl', '.text-xl', '.text-lg'
  ];
  
  contentSelectors.forEach(selector => {
    const elements = document.querySelectorAll(selector);
    elements.forEach((element) => {
      if (element.textContent && !element.classList.contains('translated')) {
        const originalText = element.textContent.trim();
        const translated = translateText(originalText, targetLanguage);
        if (translated !== originalText) {
          element.textContent = translated;
          element.classList.add('translated');
        }
      }
    });
  });
  
  console.log(`Page translated to ${targetLanguage}`);
}